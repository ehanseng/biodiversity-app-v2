import React, { createContext, useState, useContext, useEffect } from 'react';
import { supabase, clearCorruptedSession } from '../config/supabase';
import TreeStorageService from '../services/TreeStorageService';
import eventEmitter, { EVENTS } from '../utils/EventEmitter';

const AuthContext = createContext({});

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [profile, setProfile] = useState(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const [syncStats, setSyncStats] = useState({ total: 0, pending: 0, synced: 0, errors: 0 });

  useEffect(() => {
    console.log('üîê AuthProvider montado. Verificando sesi√≥n...');
    let isMounted = true;

    const checkSession = async () => {
      try {
        const { data: { session } } = await supabase.auth.getSession();
        
        if (isMounted) {
          if (session?.user) {
            console.log('‚úÖ Sesi√≥n activa encontrada. Cargando datos...');
            setUser(session.user);
            await fetchProfile(session.user.id);
            performAutoSync(session.user.id).catch(console.warn);
          } else {
            console.log('‚ùå No hay sesi√≥n activa.');
            setLoading(false);
          }
        }
      } catch (e) {
        console.error('üö® Error cr√≠tico verificando sesi√≥n:', e);
        if (isMounted) setLoading(false);
      }
    };

    checkSession();

    const { data: { subscription } } = supabase.auth.onAuthStateChange((_event, session) => {
      if (!isMounted) return;
      
      console.log(`üîî Evento de Auth recibido: ${_event}`);
      setUser(session?.user ?? null);
      if (!session?.user) {
        setProfile(null);
        setLoading(false);
      }
    });

    return () => {
      console.log('üßπ AuthProvider desmontado. Limpiando suscripci√≥n.');
      isMounted = false;
      subscription.unsubscribe();
    };
  }, []);

  const fetchProfile = async (userId) => {
    console.log('üìù Iniciando fetchProfile para:', userId);
    try {
      console.log('üì° Consultando perfil en base de datos...');
      const { data, error } = await supabase
        .from('profiles')
        .select('*')
        .eq('id', userId)
        .single();

      console.log('üìä Resultado consulta:', { hasData: !!data, hasError: !!error, errorCode: error?.code });

      if (error && error.code === 'PGRST116') {
        console.log('üÜï Creando perfil por defecto...');
        const { data: { user: currentUser } } = await supabase.auth.getUser();
        const userName = currentUser?.user_metadata?.full_name || 
                        currentUser?.email?.split('@')[0] || 
                        'Usuario';
        
        const { data: newProfile } = await supabase
          .from('profiles')
          .insert([
            {
              id: userId,
              full_name: userName,
              role: 'explorer',
              created_at: new Date().toISOString(),
            },
          ])
          .select()
          .single();

        if (newProfile) {
          console.log('‚úÖ Perfil creado exitosamente');
          setProfile(newProfile);
        }
      } else if (!error) {
        console.log('‚úÖ Perfil obtenido exitosamente');
        setProfile(data);
      }
    } catch (error) {
      console.error('‚ùå Error en fetchProfile:', error);
    }
    
    console.log('üèÅ Terminando fetchProfile - setLoading(false)');
    setLoading(false);
  };

  // Funci√≥n para reintentar inicializaci√≥n
  const retryInitialization = async () => {
    setLoading(true);
    setError(null);
    
    try {
      console.log(' Reintentando inicializaci√≥n...');
      await clearCorruptedSession();
      
      // Forzar nueva sesi√≥n
      const { data: { session }, error } = await supabase.auth.getSession();
      
      if (error) {
        throw error;
      }
      
      if (session?.user) {
        setUser(session.user);
        await fetchProfile(session.user.id);
      } else {
        setUser(null);
        setProfile(null);
        setLoading(false);
      }
    } catch (error) {
      console.error(' Error en reintento:', error);
      setError('Error al reintentar. Intenta refrescar la p√°gina.');
      setLoading(false);
    }
  };

  // Sincronizaci√≥n autom√°tica
  const performAutoSync = async (userId) => {
    try {
      console.log(' Iniciando sincronizaci√≥n autom√°tica...');
      
      // Agregar timeout para sincronizaci√≥n
      const syncTimeout = new Promise((_, reject) => {
        setTimeout(() => reject(new Error('Timeout en sincronizaci√≥n')), 15000);
      });
      
      const syncProcess = async () => {
        // Obtener estad√≠sticas antes de sincronizar
        const statsBefore = await TreeStorageService.getSyncStats();
        console.log(' Stats antes de sync:', statsBefore);
        setSyncStats(statsBefore);
        
        if (statsBefore.pending > 0) {
          console.log(` Sincronizando ${statsBefore.pending} √°rboles pendientes...`);
          // Sincronizar √°rboles pendientes
          const syncResult = await TreeStorageService.syncAllPendingTrees(userId);
          
          // Actualizar estad√≠sticas despu√©s de sincronizar
          const statsAfter = await TreeStorageService.getSyncStats();
          setSyncStats(statsAfter);
          
          console.log(` Sincronizaci√≥n completada: ${syncResult.successful}/${syncResult.total} √°rboles sincronizados`);
        } else {
          console.log(' No hay √°rboles pendientes para sincronizar');
        }
      };
      
      await Promise.race([syncProcess(), syncTimeout]);
      
    } catch (error) {
      console.error(' Error en sincronizaci√≥n autom√°tica:', error);
      if (error.message === 'Timeout en sincronizaci√≥n') {
        console.warn(' Timeout en sincronizaci√≥n - continuando sin bloquear');
      }
      // No bloquear el proceso principal si falla la sincronizaci√≥n
    }
  };

  // Funci√≥n para obtener todos los √°rboles
  const getAllTrees = async () => {
    if (!user) return [];
    try {
      return await TreeStorageService.getAllTrees(user.id);
    } catch (error) {
      console.error(' Error obteniendo √°rboles:', error);
      return [];
    }
  };

  // Funci√≥n para forzar sincronizaci√≥n manual
  const forceSyncTrees = async () => {
    if (!user) {
      console.warn('‚ö†Ô∏è No hay usuario para forzar sync');
      return false;
    }
    
    console.log('‚ö° Forzando sincronizaci√≥n manual...');
    try {
      // Primero, intentar obtener √°rboles de la base de datos
      const dbTrees = await TreeStorageService.getTreesFromDatabase();
      console.log(`‚òÅÔ∏è ${dbTrees.length} √°rboles obtenidos de la nube.`);
      
      // Luego, sincronizar los √°rboles locales pendientes
      const localStats = await TreeStorageService.getSyncStats();
      if (localStats.pending > 0) {
        console.log(`üì§ Sincronizando ${localStats.pending} √°rboles locales...`);
        await TreeStorageService.syncAllPendingTrees(user.id);
      }
      
      // Finalmente, emitir evento para que la UI se actualice
      eventEmitter.emit(EVENTS.DATA_REFRESH_NEEDED);
      console.log('‚úÖ Sincronizaci√≥n manual completada.');
      return true;
    } catch (error) {
      console.error('‚ùå Error en sincronizaci√≥n manual forzada:', error);
      return false;
    }
  };

  const signIn = async (email, password) => {
    try {
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });
      if (error) throw error;
      return { success: true };
    } catch (error) {
      return { success: false, error: error.message };
    }
  };

  const signUp = async (email, password, fullName, role = 'explorer') => {
    try {
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
      });

      if (error) throw error;

      if (data.user) {
        // Create profile
        const { error: profileError } = await supabase
          .from('profiles')
          .insert([
            {
              id: data.user.id,
              full_name: fullName,
              role: role,
              created_at: new Date().toISOString(),
            },
          ]);

        if (profileError) throw profileError;
      }

      return { success: true };
    } catch (error) {
      return { success: false, error: error.message };
    }
  };

  const signOut = async () => {
    try {
      console.log(' Cerrando sesi√≥n...');
      
      // Cerrar sesi√≥n en Supabase
      await supabase.auth.signOut();
      
      // Limpiar estado inmediatamente
      setUser(null);
      setProfile(null);
      setSyncStats({ total: 0, pending: 0, synced: 0, errors: 0 });
      setLoading(false);
      setError(null);
      
      // Recargar p√°gina inmediatamente
      window.location.reload();
      
      return { success: true };
    } catch (error) {
      console.error(' Error:', error);
      // Forzar recarga incluso con error
      window.location.reload();
      return { success: false, error: error.message };
    }
  };

  // Funci√≥n para refrescar perfil manualmente
  const refreshProfile = async () => {
    if (!user) return;
    try {
      console.log(' Refrescando perfil...');
      await fetchProfile(user.id);
    } catch (error) {
      console.error(' Error refrescando perfil:', error);
    }
  };

  // Funci√≥n para validar integridad de sesi√≥n
  const validateSessionIntegrity = async (user) => {
    try {
      // Verificar que el usuario tenga datos b√°sicos
      if (!user || !user.id || !user.email) {
        console.warn(' Usuario sin datos b√°sicos');
        return false;
      }
      
      // Intentar hacer una consulta simple para verificar conectividad
      const { error } = await supabase
        .from('profiles')
        .select('id')
        .eq('id', user.id)
        .limit(1);
      
      if (error && error.code !== 'PGRST116') {
        console.warn(' Error de conectividad con base de datos:', error);
        return false;
      }
      
      return true;
    } catch (error) {
      console.error(' Error validando sesi√≥n:', error);
      return false;
    }
  };

  const value = {
    user,
    profile,
    loading,
    error,
    syncStats,
    signIn,
    signUp,
    signOut,
    getAllTrees,
    forceSyncTrees,
    retryInitialization,
    refreshProfile,
    performAutoSync: () => performAutoSync(user?.id),
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};
